export function applyLanguage(lang) {
    localStorage.setItem("preferredLanguage", lang);
    
    // Update all language button display texts
    const langBtns = document.querySelectorAll(".lang-btn");
    langBtns.forEach(btn => {
        btn.innerHTML = lang === "hi" ? "🌐 HI" : "🌐 EN";
    });

    if (lang === "en") {
        // Reload page to restore original English text easily
        location.reload();
        return;
    }

    // Traverse and translate
    const dict = translations[lang];
    if (!dict) return;

    // Helper to translate text contents
    function translateElement(el) {
        const text = el.innerText ? el.innerText.trim() : "";
        if (dict[text]) {
            el.innerText = dict[text];
            return;
        }

        // Try replacing placeholder attributes
        if (el.placeholder && dict[el.placeholder]) {
            el.placeholder = dict[el.placeholder];
        }

        // Translate child nodes
        for (let child of el.childNodes) {
            if (child.nodeType === Node.TEXT_NODE) {
                const childText = child.nodeValue.trim();
                if (dict[childText]) {
                    child.nodeValue = child.nodeValue.replace(childText, dict[childText]);
                }
            } else if (child.nodeType === Node.ELEMENT_NODE) {
                // Skip script and style tags
                if (child.tagName !== "SCRIPT" && child.tagName !== "STYLE") {
                    translateElement(child);
                }
            }
        }
    }

    // Run translation on body
    translateElement(document.body);
}

// Initial load check
document.addEventListener("DOMContentLoaded", () => {
    const savedLang = localStorage.getItem("preferredLanguage") || "en";
    if (savedLang === "hi") {
        // Wait a tiny bit for page setup
        setTimeout(() => applyLanguage("hi"), 100);
    }

    // Bind event listeners to language button
    document.addEventListener("click", (e) => {
        if (e.target.classList.contains("lang-btn")) {
            const currentLang = localStorage.getItem("preferredLanguage") || "en";
            const newLang = currentLang === "en" ? "hi" : "en";
            applyLanguage(newLang);
        }
    });
});
